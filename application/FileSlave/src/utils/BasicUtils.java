package utils;

public interface BasicUtils {
	
	public static void setArrayZero(int [] data)
	{
		for(int i = 0; i < data.length; ++i) data[i] = 0;
	}
	public static void setArrayZero(byte [] data)
	{
		for(int i = 0; i < data.length; ++i) data[i] = 0;
	}
	public static void setArrayZero(short [] data)
	{
		for(int i = 0; i < data.length; ++i) data[i] = 0;
	}
	public static void setArrayZero(char [] data)
	{
		for(int i = 0; i < data.length; ++i) data[i] = 0;
	}
	public static void setArrayZero(double [] data)
	{
		for(int i = 0; i < data.length; ++i) data[i] = 0;
	}
	public static void setArrayZero(float [] data)
	{
		for(int i = 0; i < data.length; ++i) data[i] = 0;
	}
}
